/* matrix state(1:on, 0:off) */
static matrix_row_t matrix[MATRIX_ROWS];


__attribute__ ((weak))
void matrix_init_kb(void) {
    matrix_init_user();
}

__attribute__ ((weak))
void matrix_scan_kb(void) {
    matrix_scan_user();
}

__attribute__ ((weak))
void matrix_init_user(void) {
}

__attribute__ ((weak))
void matrix_scan_user(void) {
}

inline
uint8_t matrix_rows(void) {
    return MATRIX_ROWS;
}

inline
uint8_t matrix_cols(void) {
    return MATRIX_COLS;
}
uint8_t addresses[][6] = {"1Node", "2Node"};
void matrix_init(void) {
    matrix_init_quantum();
    wait_ms(2000);
    println("Hello World! I am GOD");
    nrf_init(B5, B6);
    openReadingPipe(1, addresses[0]);
    openReadingPipe(2, addresses[1]);
    startListening();
    println("DONE INIT");
}

uint8_t matrix_scan(void)
{
    // println("Matrix Scan");
    uint8_t pipe = qmkrf_available();
    uint8_t extraShift=0;
    if(pipe <= 5) {
        if(pipe == 2) extraShift = 5;
        uint8_t text[32];
        qmkrf_read(&text[0], 32); //Read all 32 and throw away the rest
        for(int i=0; i<5; i++) {
            uint8_t curr = text[i];
            for(int j=0; j<5; j++) {
                matrix[j] = (matrix[j]&~(1<<(i+extraShift))) | ((curr&1)<<(i+extraShift));
                curr >>=1;
            }
        }
        matrix_print();
    }
    matrix_scan_quantum();
    return 1;
}

inline
bool matrix_is_on(uint8_t row, uint8_t col)
{
    return (matrix[row] & ((matrix_row_t)1<<col));
}

inline
matrix_row_t matrix_get_row(uint8_t row)
{
    return matrix[row];
}

void matrix_print(void)
{
    print_matrix_header();

    for (uint8_t row = 0; row < MATRIX_ROWS; row++) {
        print_hex8(row); print(": ");
        print_matrix_row(row);
        print("\n");
    }
}

uint8_t matrix_key_count(void)
{
    uint8_t count = 0;
    for (uint8_t i = 0; i < MATRIX_ROWS; i++) {
        count += matrix_bitpop(i);
    }
    return count;
}
